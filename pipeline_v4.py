# NODE 3 — FORMAT RESPONSE (Model 2)
# Model 2 formats final answer — sees SAP data, always local
# Also includes the ABAP query generated by Model 1
# ══════════════════════════════════════════════════════════════════════════════
def node_format(state: AgentState) -> AgentState:
    intent      = state["intent"]
    tool_result = state["tool_result"]
    question    = state["question"]
    abap        = state["abap_query"]

    if intent == "semantic":
        prompt = PromptTemplate(
            template="""You are an SAP SD assistant. Answer using ONLY the records below.
Status: GBSTK A=Open B=In-Process C=Completed | RFBSK A=Open B=Partial C=Cleared
Currency: INR | Fiscal Year: Indian Apr-Mar

SAP Records:
{context}

Question: {question}
Answer:""",
            input_variables=["context", "question"]
        )
        answer = (prompt | llm_2 | StrOutputParser()).invoke({
            "context": tool_result, "question": question
        })
    else:
        prompt = PromptTemplate(
            template="""You are an SAP data analyst. Your ONLY job is to reformat the data below as bullet points.

STRICT RULES:
- Copy EVERY single line from SAP Data — do NOT skip any line
- Do NOT combine any entries
- Do NOT summarize or calculate
- Do NOT add or remove any numbers
- Keep exact names and values as shown
- Format each line starting with "• "

SAP Data:
{data}

Question: {question}

Bullet point list (include ALL lines, do not stop early):""",
            input_variables=["data", "question"]
        )
        answer = (prompt | llm_2 | StrOutputParser()).invoke({
            "data": tool_result, "question": question
        })

    # append ABAP query to answer
    mongo_q = state.get("mongo_query", "")
    final   = (
        f"{answer}\n\n"
        f"---\n"
        f"MongoDB Query:\n{mongo_q}\n\n"
        f"ABAP Query:\n{abap}"
    )
    return {**state, "final_answer": final}

# ══════════════════════════════════════════════════════════════════════════════
# ROUTER
# ══════════════════════════════════════════════════════════════════════════════
def route(state: AgentState) -> Literal["node_aggregate","node_lookup","node_hybrid_search"]:
    intent = state.get("intent", "semantic")
    if intent == "aggregate": return "node_aggregate"
    if intent == "lookup":    return "node_lookup"
    return "node_hybrid_search"

# ══════════════════════════════════════════════════════════════════════════════
# BUILD LANGGRAPH
# ══════════════════════════════════════════════════════════════════════════════
def build_agent():
    g = StateGraph(AgentState)
    g.add_node("node_classify",      node_classify)       # Model 1
    g.add_node("node_aggregate",     node_aggregate)      # Model 2
    g.add_node("node_lookup",        node_lookup)         # Model 2
    g.add_node("node_hybrid_search", node_hybrid_search)  # Model 2
    g.add_node("node_format",        node_format)         # Model 2
    g.set_entry_point("node_classify")
    g.add_conditional_edges("node_classify", route, {
        "node_aggregate":     "node_aggregate",
        "node_lookup":        "node_lookup",
        "node_hybrid_search": "node_hybrid_search",
    })
    g.add_edge("node_aggregate",     "node_format")
    g.add_edge("node_lookup",        "node_format")
    g.add_edge("node_hybrid_search", "node_format")
    g.add_edge("node_format",        END)
    return g.compile()

agent = build_agent()

