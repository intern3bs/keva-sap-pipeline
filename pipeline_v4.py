# NODE 2B — LOOKUP TOOL (Model 2)
# ══════════════════════════════════════════════════════════════════════════════
def node_lookup(state: AgentState) -> AgentState:
    entity_id = str(state["params"].get("entity_id") or "").upper()

    if re.match(r'SO\d+', entity_id):
        doc   = db["VBAK"].find_one({"VBELN": entity_id}, {"_id": 0})
        items = list(db["VBAP"].find({"VBELN": entity_id}, {"_id": 0}))
