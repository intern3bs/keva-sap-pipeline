# NODE 2C — HYBRID SEARCH (Vector + Text + RRF) — Model 2
# ══════════════════════════════════════════════════════════════════════════════
def reciprocal_rank_fusion(lists: list, k: int = 60) -> list:
    scores, texts = {}, {}
    for results in lists:
        for rank, doc in enumerate(results, start=1):
            did = doc["id"]
            if did not in scores:
                scores[did] = 0.0
                texts[did]  = doc["text"]
            scores[did] += 1.0 / (k + rank)
    return [{"id": d, "text": texts[d], "score": scores[d]}
            for d in sorted(scores, key=lambda x: scores[x], reverse=True)]

def get_tables(question: str) -> list:
    q = question.lower()
    if any(w in q for w in ["margin", "cost", "profit"]):             return ["VBAP", "VBRP"]
    if any(w in q for w in ["manager", "region", "area", "territory"]):return ["VBAK", "KNA1"]
    if any(w in q for w in ["order", "sales"]):                        return ["VBAK", "VBAP"]
    if any(w in q for w in ["deliver", "shipment"]):                   return ["LIKP", "LIPS"]
    if any(w in q for w in ["bill", "invoice"]):                       return ["VBRK", "VBRP"]
    if any(w in q for w in ["customer", "client"]):                    return ["KNA1", "KNVV"]
    return ["VBAK", "VBAP", "VBRK", "KNA1"]

def node_hybrid_search(state: AgentState) -> AgentState:
    question = state["question"]
    tables   = get_tables(question)

    # vector search
    vec = []
    for t in tables:
        for doc in vectordb.similarity_search(question, k=5, filter={"table": t}):
            vec.append({"id": f"{t}_{hash(doc.page_content)}", "text": doc.page_content})

    # text search
    txt = []
    for t in [x for x in tables if x in ["VBAK","VBAP","VBRK","KNA1","LIKP"]]:
        try:
            for doc in db[t].find({"$text": {"$search": question}},
                                   {"score": {"$meta": "textScore"}, "_id": 0}
                                  ).sort([("score", {"$meta": "textScore"})]).limit(5):
                text = " | ".join(f"{k}: {v}" for k, v in doc.items() if k != "score" and v)
                txt.append({"id": f"{t}_t_{hash(text)}", "text": text[:500]})
        except Exception:
            pass

    fused   = reciprocal_rank_fusion([vec, txt])[:10]
    context = "\n\n---\n\n".join(r["text"] for r in fused)
    return {**state, "tool_result": context}

