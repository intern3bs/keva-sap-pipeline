def node_aggregate(state: AgentState) -> AgentState:
    p      = state["params"]
    sub    = state["sub_type"]
    mongo_q = get_mongo_query_text(sub, p) 
    n      = int(p.get("n") or 5)
    fy1    = str(p.get("fy1") or "FY2023-24")
    fy2    = str(p.get("fy2") or "FY2024-25")
    thresh = float(p.get("threshold") or 20)
    amount = p.get("amount")
    status = p.get("status")
    result = ""

    if sub == "top_customers":
        rows = list(db["VBRK"].aggregate([
            {"$match": {"FKSTO": {"$ne": "X"}}},
            {"$group": {"_id": "$KUNNR", "NAME1": {"$first": "$NAME1"}, "total": {"$sum": "$NETWR"}}},
            {"$sort": {"total": -1}}, {"$limit": n}
        ]))
        lines = [f"Top {n} customers by total invoiced value:\n"]
        for i, r in enumerate(rows, 1):
            lines.append(f"  {i}. {r['NAME1']:35s}  ₹{r['total']:>15,.2f}")
        result = "\n".join(lines)

    elif sub == "top_products":
        rows = list(db["VBAP"].aggregate([
            {"$group": {"_id": "$MATNR", "ARKTX": {"$first": "$ARKTX"},
                        "qty": {"$sum": "$KWMENG"}, "revenue": {"$sum": "$NETWR"}}},
            {"$sort": {"qty": -1}}, {"$limit": n}
        ]))
        lines = [f"Top {n} selling products by quantity:\n"]
        for i, r in enumerate(rows, 1):
            lines.append(f"  {i}. {r['ARKTX']:40s}  Qty: {r['qty']:>6,}  |  ₹{r['revenue']:>15,.2f}")
        result = "\n".join(lines)

    elif sub == "customer_growth":
        d1 = {r["_id"]: r for r in db["VBAK"].aggregate([
            {"$match": {"FISCAL_YEAR": fy1}},
            {"$group": {"_id": "$KUNNR", "NAME1": {"$first": "$NAME1"}, "rev": {"$sum": "$NETWR"}}}
        ])}
        d2 = {r["_id"]: r for r in db["VBAK"].aggregate([
            {"$match": {"FISCAL_YEAR": fy2}},
            {"$group": {"_id": "$KUNNR", "NAME1": {"$first": "$NAME1"}, "rev": {"$sum": "$NETWR"}}}
        ])}
        growth = []
        for k, r in d2.items():
            if k in d1 and d1[k]["rev"] > 0:
                pct = (r["rev"] - d1[k]["rev"]) / d1[k]["rev"] * 100
                if pct > thresh:
                    growth.append({"name": r["NAME1"], "fy1": d1[k]["rev"], "fy2": r["rev"], "pct": pct})
        growth.sort(key=lambda x: x["pct"], reverse=True)
        lines = [f"Customers with >{thresh:.0f}% growth ({fy1} → {fy2}):\n"]
        if not growth:
            lines.append("  No customers found with this growth threshold.")
        for r in growth:
            lines.append(f"  • {r['name']:35s}  {fy1}: ₹{r['fy1']:>12,.0f}  →  {fy2}: ₹{r['fy2']:>12,.0f}  |  {r['pct']:>+.1f}%")
        result = "\n".join(lines)

    elif sub == "product_margins":
        rows = list(db["VBAP"].aggregate([
            {"$group": {"_id": "$MATNR", "ARKTX": {"$first": "$ARKTX"},
                        "avg_margin": {"$avg": "$MARGIN_PCT"}, "total_rev": {"$sum": "$NETWR"}}},
            {"$sort": {"avg_margin": 1}}, {"$limit": n}
        ]))
        lines = [f"{n} products with least margins:\n"]
        for i, r in enumerate(rows, 1):
            lines.append(f"  {i}. {r['ARKTX']:40s}  Avg Margin: {r['avg_margin']:>5.1f}%  |  Revenue: ₹{r['total_rev']:>12,.0f}")
        result = "\n".join(lines)

    elif sub == "company_product":
        rows = list(db["VBAP"].aggregate([
            {"$group": {"_id": {"kunnr": "$KUNNR", "matnr": "$MATNR"},
                        "NAME1": {"$first": "$NAME1"}, "ARKTX": {"$first": "$ARKTX"},
                        "revenue": {"$sum": "$NETWR"}}},
            {"$sort": {"revenue": -1}}, {"$limit": n}
        ]))
        lines = [f"Top {n} company-product combinations by revenue:\n"]
        for i, r in enumerate(rows, 1):
            lines.append(f"  {i:2}. {r['NAME1']:30s}  |  {r['ARKTX']:35s}  |  ₹{r['revenue']:>12,.0f}")
        result = "\n".join(lines)

    elif sub == "manager_growth":
        d1 = {r["_id"]: r["rev"] for r in db["VBAK"].aggregate([
            {"$match": {"FISCAL_YEAR": fy1}},
            {"$group": {"_id": "$AREA_MGR", "rev": {"$sum": "$NETWR"}}}
        ])}
        d2 = {r["_id"]: r["rev"] for r in db["VBAK"].aggregate([
            {"$match": {"FISCAL_YEAR": fy2}},
            {"$group": {"_id": "$AREA_MGR", "rev": {"$sum": "$NETWR"}}}
        ])}
        growth = []
        for am, rev2 in d2.items():
            if am in d1 and d1[am] > 0:
                pct = (rev2 - d1[am]) / d1[am] * 100
                growth.append({"am": am, "fy1": d1[am], "fy2": rev2, "pct": pct})
        growth.sort(key=lambda x: x["pct"])
        lines = [f"Area manager growth ({fy1} → {fy2}), slowest first:\n"]
        for r in growth:
            lines.append(f"  {r['am']:25s}  {fy1}: ₹{r['fy1']:>12,.0f}  →  {fy2}: ₹{r['fy2']:>12,.0f}  |  {r['pct']:>+.1f}%")
        result = "\n".join(lines)

    elif sub == "order_status":
        smap  = {"open": "A", "in process": "B", "in-process": "B", "completed": "C"}
        gbstk = smap.get(str(status).lower(), "A")
        rows  = list(db["VBAK"].find({"GBSTK": gbstk},
                     {"VBELN":1,"NAME1":1,"NETWR":1,"ERDAT":1,"FISCAL_YEAR":1,"_id":0}))
        lines = [f"Found {len(rows)} {status} sales orders:\n"]
        for r in rows:
            lines.append(f"  • {r['VBELN']}  |  {r['NAME1']}  |  ₹{r['NETWR']:,.2f}  |  {r['FISCAL_YEAR']}  |  {r['ERDAT']}")
        result = "\n".join(lines)

    elif sub == "delivery_status":
        smap  = {"pending": "A", "not started": "A", "partial": "B", "completed": "C"}
        lfgsx = smap.get(str(status).lower(), "A")
        rows  = list(db["LIKP"].find({"LFGSX": lfgsx},
                     {"VBELN":1,"NAME1":1,"WADAT":1,"VBELN_VK":1,"_id":0}))
        lines = [f"Found {len(rows)} deliveries (status: {status}):\n"]
        for r in rows:
            lines.append(f"  • {r['VBELN']}  |  {r['NAME1']}  |  Ref: {r['VBELN_VK']}  |  Planned: {r['WADAT']}")
        result = "\n".join(lines)

    elif sub == "billing_status":
        smap  = {"open": "A", "not cleared": "A", "partial": "B", "cleared": "C"}
        rfbsk = smap.get(str(status).lower(), "A")
        rows  = list(db["VBRK"].find({"RFBSK": rfbsk, "FKSTO": {"$ne": "X"}},
                     {"VBELN":1,"NAME1":1,"NETWR":1,"MWSBP":1,"ERDAT":1,"_id":0}))
        lines = [f"Found {len(rows)} billing documents (status: {status}):\n"]
        for r in rows:
            total = round(r["NETWR"] + r["MWSBP"], 2)
            lines.append(f"  • {r['VBELN']}  |  {r['NAME1']}  |  ₹{total:,.2f}  |  {r['ERDAT']}")
        result = "\n".join(lines)

    elif sub == "revenue_by_fy":
        rows = list(db["VBAK"].aggregate([
            {"$group": {"_id": "$FISCAL_YEAR", "total": {"$sum": "$NETWR"}, "count": {"$sum": 1}}},
            {"$sort": {"_id": 1}}
        ]))
        lines = ["Revenue by fiscal year:\n"]
        for r in rows:
            lines.append(f"  {r['_id']}:  {r['count']} orders  |  ₹{r['total']:>15,.2f}")
        result = "\n".join(lines)

    elif sub == "orders_above" and amount:
        rows  = list(db["VBAK"].find({"NETWR": {"$gt": float(amount)}, "GBSTK": "A"},
                     {"VBELN":1,"NAME1":1,"NETWR":1,"VDATU":1,"_id":0}).sort("NETWR", -1))
        lines = [f"Found {len(rows)} open orders above ₹{float(amount):,.0f}:\n"]
        for r in rows:
            lines.append(f"  • {r['VBELN']}  |  {r['NAME1']}  |  ₹{r['NETWR']:,.2f}  |  Delivery: {r['VDATU']}")
        result = "\n".join(lines)

    else:
        result = f"Sub-type '{sub}' not matched — routing to hybrid search."

    return {**state, "tool_result": result, "mongo_query": mongo_q}

