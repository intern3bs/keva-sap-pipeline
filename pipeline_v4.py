def normalize_fiscal_years(params: dict, question: str = "") -> dict:
    """
    Extract FY directly from question if short notation detected.
    Overrides whatever model returned.
    """
    # find all short FY mentions in question e.g. FY24, FY 25
    short_fys = re.findall(r'\bFY\s*(\d{2})\b', question, re.I)
    
    if short_fys:
        # convert short FY to full format
        full_fys = []
        for yy in short_fys:
            year  = 2000 + int(yy)
            start = year - 1
            full_fys.append(f"FY{start}-{yy}")
        
        # assign fy1 and fy2 directly from question
        if len(full_fys) >= 1:
            params["fy1"] = full_fys[0]
        if len(full_fys) >= 2:
            params["fy2"] = full_fys[1]
        return params

    # no short FY in question — don't touch params
    return params

