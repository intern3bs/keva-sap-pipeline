                lines.append(f"    • {item.get('ARKTX'):40s}  Qty: {item.get('KWMENG')} {item.get('MEINS')}  |  ₹{item.get('NETWR',0):,.2f}  |  Margin: {item.get('MARGIN_PCT',0):.1f}%")
